    private void modelarTabela(){
        modelo.addColumn("NUMERO");
        modelo.addColumn("TIPO");
        tblTelefones.setModel(modelo);
    }
    
    private void preencherTabela(Contato c){
        modelo.setNumRows(0);
        for(Telefone t: c.getTelefones()){
            modelo.addRow(new Object[]{
                t.getNumero(),
                t.getTipoTelefone()
            });
        }
    }
